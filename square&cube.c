//Accept any three number and find thair squares and cubes...
#include<stdio.h>
int main()
{
	int n1,n2,n3;
	printf("\nEnter first number:");
	scanf("%d",&n1);
	printf("Square = %d and Cube = %d",(n1*n1),(n1*n1*n1));
	
	printf("\nEnter second number:");
	scanf("%d",&n2);
	printf("Square = %d and Cube = %d",(n2*n2),(n2*n2*n2));
	
	printf("\nEnter third number:");
	scanf("%d",&n3);
	printf("Square = %d and Cube = %d",(n3*n3),(n3*n3*n3));
}
